class Modify
{
  public static void main(String args [])
  {
  int n1,n2,sum,sub,mul,div,remainder;
  n1=125;
  n2=24;
  sum=n1+n2;
  sub=n1-n2;
  mul=n1*n2;
  div=n1/n2;
  remainder=n1%n2;
  System.out.println(sum);
  System.out.println(sub);
  System.out.println(mul);
  System.out.println(div);
  System.out.println(remainder);
  }
} 